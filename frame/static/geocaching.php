<h1>Geocaching</h1>

<h2>Vorstellung des Projektes</h2>
<dl class='inline'>
Dieses Projekt ist im Rahmen des PEM-Seminars im Sommersemester 2014 entstanden.
Wir haben für euch insgesamt 5 Caches in Potsdam versteckt. Diese Caches sind Filmdosen, die jeweils ein Logbuch, eine kurze Information und ein Rätsel zur Mathematik/Informatik enthalten.
Die Caches befinden sich alle auf dem 52. Breitengrad und dem 13. Längengrad. Um die Nachkommastellen der Koordinaten des jeweils nächsten Caches zu erhalten, ist das jeweilige Rätsel zu lösen.
</dl>
<br>Damit die Geocaches erhalten bleiben und auch in Zukunft gefunden werden können, bitten wir euch die folgenden Regeln einzuhalten:</br>
1) Als Beweis, dass der Cache gefunden wurde, kann sich der Finder in das beiliegende Logbuch eintragen.
<br>2) Der Cache muss wieder genau dort platziert werden, wo er gefunden wurde.</br>
3) Sämtliche Inhalte (auch das Rätsel und der Hinweis zum Gesamträtsel) müssen wieder mit in den Cache gelegt werden.
<br>Damit ihr eure Lösungen überprüfen könnt, findet hier nachfolgend die Quersummen der Lösungen:</br>
1. Vielfache: 23
<br>2. Fibonacci: 26</br>
3. Palindrom: 30
<br>4. Wochentage: 9</br>
5. Sudoku: 15
<br>Die Datei für Rätsel 5 findest du hier: <a href="/static-files/geocaching/sudoku.txt">sudoku.txt</a></br>

<h2>Koordinaten des 1. Caches</h2>
<dl class='inline'>
<h5>Um die Nachkommastellen der Koordinaten des ersten Caches zu erhalten, löse folgendes Rätsel:</h5>
Die Zahlen unter 10, die ein Vielfaches von 3 oder 5 sind, sind 3, 5, 6 und 9. Die Summe dieser Zahlen ist 23.
</dl>
<br>Finde die Summe der Zahlen, die kleiner als 1000 sind und die Vielfache von 3 oder 5 sind.</br>
Um die Nachkommastellen der Breitenkoordinate zu erhalten, berechne den Rest deiner Lösung modulo 193442 und für die Längenkoordinate den Rest modulo 216343.

